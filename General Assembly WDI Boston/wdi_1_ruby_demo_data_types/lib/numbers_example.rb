def add_numbers(a, b)
  a + b
end

def subtract_numbers(a, b)
  a - b
end

def multiply_numbers(a, b)
  a * b
end

def divide_with_slash(a, b)
  a / b
end

def modulo_division(a, b)
  a % b
end

def a_greater_than_b?(a, b)
  a > b
end

def a_equals_b?(a, b)
  a == b
end

def as_a_float(a)
  a.to_f
end

def as_a_fixnum(a)
  a.to_i
end
